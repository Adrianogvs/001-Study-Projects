# Tipo de dados int
num = int(input('Numero: '))
print(type(num), num)

# Tipo de dados float
num2 = float(input('Numero: '))
print(type(num2), num2)

# Tipo de dados str
num3 = str(input("String: "))
print(type(num3), num3)

# Tipo de dados Bool
num4 = bool(num == num2)
print(type(num4), num4)